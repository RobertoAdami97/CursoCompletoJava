package com.excript.aula0015;

public class Aula0015 {

	public static void main(String[] args) {
		
		/*
		 * CONSTANTES
		 * 1) Os dados não poderam ser mudados no tempo de execução do sistema
		 * 2) uma const. tem o seu endereco protegido
		 * 3) uma const. nunca será desalocada
		 * 4) declaramos uma vez, utilizamos quantas vezes for preciso
		 * */
		
		int var = 100;// declara variavel
		final int constante = 0;
		
		System.out.println(var);
		System.out.println(constante);
		
		//var = 1;
		//constante = 2;
		
		//ATENÇÃO: CTRL + F11 executa o codigo.

	}

}
