package com.excript.aula0019;

public class Aula0019 {

	public static void main(String[] args) {
		
		// por padrão uma variavel do tipo boolean sempre será false
		// o boolean ocupa 1 byte ou 8 bits
		boolean b = true;
				
		// por padrão o char é igual a vazio, ocupa 2 bytes ou 16 bits
		char c = 'a';
		char c2 = '1';
		
		////por padrão no tipo short, o java atribui 0 a nossa variavel
		//ocupa 1 byte na memória ou 8 bits
		//o byte vai dos valores -128 até +127
		byte Byte = 100;
		//Byte = 128;
		
		//por padrão no tipo short, o java atribui 0 a nossa variavel
		//-32768 até +32767
		short camisa = 2000;
		
		//por padrão no tipo short, o java atribui 0 a nossa variavel
		//-2,147,483,648 até +2,147,486,647
		int i = 1;
		
		long l;
		
		//para o tipo float, apos o valor tem que botar a letra f quando usar casas decimais
		float f = 4.4f;
		double d = 2;
	
		

	}

}
