package com.excript.expressoeslambda;

public class ExpressoesLambda {

	public static void main(String[] args) {
		
		
		
//		O que são as expressões lambda?
//		
//		são métodos anonimos, porém esse metodos nao são executados em si proprios.
//		As expressões lambdas podem ser chamadas de metodos anonimos, closures, tudo sao sinonimos.
//		Então, nós temos que as expressões lambdas são funcões que nao possuem nomes.
		
//		Com o advento das expressões lambdas, se fala muito do paradigma funcional.
//		Pode-se entender o paradigma funcional como se for uma forma de programar onde
//		existe um conjunto de funções, que podem ser verificadas para obter um determinado resultado.
//		
		
//		A segunda parte que temos que estudar para conseguir entender as expressões lambdas, 
//		são as Interfaces Funcionais.
//		
//		Uma interface funcional é aquele que contem somente um metodo abstrato. Normalmente,
//		esse metodo especifica a finalidade dessa interface.
//		
//		Cada interface funcional tera sempre um metodo abstrato e este metodo sera chamado de
//		metodo funcional da interface funcional.
//				
//		As interfaces funcionais geralmente apresentarão a anotação @functionInterface,
//		a utilização dessa anotação não é um requisito para que o compilador reconheça a 
//		interface funcional como uma interface funcional. temos formas distintas de 
//		interface funciona.
//		
//		Function: uma função que recebe um único argumento
//		Predicate: função que recebe um único parâmetro e devolve um booleano
//		Consumer: função que recebe um único parâmetro e não devolve nada
		
	}

}
